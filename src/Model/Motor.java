package Model;

/*
 * --This class is used to represent the motor of the mower.
 * --This class has two methods --@start() and @stop()
 * --The @start() method is used to start the motor
 * --The @stop() method is used to stop the motor
 */
public class Motor {
    public void start() {
        // Start the motor
        System.out.println("Motor started");
    }

    public void stop() {
        // Stop the motor
        System.out.println("Motor stopped");
    }
}
